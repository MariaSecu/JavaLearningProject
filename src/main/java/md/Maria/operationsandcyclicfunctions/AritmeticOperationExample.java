package md.Maria.operationsandcyclicfunctions;

public class AritmeticOperationExample {
    public static void main(String[] args) {
        int m = 12;
        int s = 5;
        //Sum
        int sum = m + s;
        System.out.println("Sum = " + sum);
        System.out.println("Sum: " + (m + s));
        //Substraction
        int sub = m - s;
        System.out.println("The result of subtraction = " + sub);
        //Multiplication
        int multiplication = m * s;
        System.out.println("The result of multiplication is: " + multiplication);
        //Devision
        int dev = m / s;
        System.out.println("The result of devision is: " + dev);
        //The rest of devision
        int rest = m % s;
        System.out.println("The rest of devision is: " + rest);
    }
}
